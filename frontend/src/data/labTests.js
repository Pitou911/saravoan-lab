// Flat map: "CategoryKey::Test Name" -> { category, test }
export function getAllTests() {
  const result = {}
  for (const [catKey, cat] of Object.entries(LAB_TESTS)) {
    for (const test of cat.tests) {
      result[`${catKey}::${test}`] = { category: cat.label, test }
    }
  }
  return result
}

// Group selected test keys by category label
export function groupSelectedTests(selectedKeys) {
  const grouped = {}
  for (const key of selectedKeys) {
    const [catKey, test] = key.split('::')
    const catLabel = LAB_TESTS[catKey]?.label || catKey
    if (!grouped[catLabel]) grouped[catLabel] = []
    grouped[catLabel].push(test)
  }
  return grouped
}
